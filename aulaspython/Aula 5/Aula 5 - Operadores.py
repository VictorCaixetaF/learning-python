'''
+  - Soma
-  - Subtração
*  - Multiplicação
/  - Divisão
// - Divisão inteira
** - Exponenciação
%  - Resto da divisão
() - equação
'''
print(10*10)
print(10+10)
print(10-5)
print(10/3)
print(10//3)
print(10 % 3)
print(10**10)
print(10 * 'Victor')
print('5'+'5')  #aqui o + concatena o str
print(5+2*10)
print((5+2)*10)
